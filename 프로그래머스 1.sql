CREATE DATABASE book;

USE book;

CREATE TABLE BOOK (
    BOOK_ID INT AUTO_INCREMENT PRIMARY KEY,
    CATEGORY VARCHAR(50),
    AUTHOR_ID INT,
    PRICE DECIMAL(10, 2),
    PUBLISHED_DATE DATE
);


